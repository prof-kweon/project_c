#include <stdio.h>
#include <stdlib.h>
#include <unistd.h> // For sleep()

// 정삼각형과 역삼각형 출력 함수
void print_triangle(int height, int reverse) {
    for (int i = 1; i <= height; i++) {
        if (reverse) {
            for (int j = 0; j < i; j++) printf(" ");
            for (int j = 0; j <= height - i; j++) printf("*");
        } else {
            for (int j = 0; j < height - i; j++) printf(" ");
            for (int j = 0; j < i; j++) printf("*");
        }
        printf("\n");
    }
}

int main() {
    char date[11];  // 날짜 입력 (yyyy-mm-dd)
    char name[50];  // 이름 입력

    // 입력 받기
    printf("[현재 날짜를 \"yyyy-mm-dd\" 형식으로 입력하세요]: ");
    scanf("%10s", date);
    printf("[당신의 이름을 입력하세요]: ");
    scanf("%s", name);

    // 입력 처리 결과 출력
    printf("**입력이 정상적으로 처리되었습니다.**\n");

    // 3초 대기 (보너스 조건)
    sleep(3);

    // 화면 지우기 (Linux 및 Windows 대응)
    #ifdef _WIN32
        system("cls");
    #else
        system("clear");
    #endif

    // 스플래시 화면 출력
    printf("+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++\n");
    print_triangle(5, 0); // 정삼각형 출력

    printf("                         [마그라테아 ver 0.1]                               \n");
    printf("           풀 한 포기 없는 황무지에서 반짝이는 행성을 만들내는 곳 마그라테아,         \n");
    printf("         사람들이 보지 못하는 잠재력을 찾고 전문가의 손길을 더해 보석을 빗는 곳,    \n");
    printf("           마그라테아에 오신걸 환영합니다.                                       \n");

    print_triangle(5, 1); // 역삼각형 출력
    printf("+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++\n");
    printf("[사용자]: %s                                   [실행 시간]: %s\n", name, date);
    printf("================================================================================\n");

    return 0;
}

